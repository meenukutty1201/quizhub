//timer
var timeleft=10;
var downloadTimer=setInterval(function(){
   if(timeleft<=0)
   {
      clearInterval(downloadTimer);
      document.getElementById("timer").innerHTML="0";
   }
   else
   {
      document.getElementById("timer").innerHTML=timeleft;
   }
   
   timeleft-=1;
},1000);


